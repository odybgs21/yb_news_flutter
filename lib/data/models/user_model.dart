import '../../domain/entities/user_entity.dart';

class UserModel extends UserEntity {
  const UserModel({
    required super.id,
    required super.email,
    super.name,
    super.isFirstLogin,
  });

  factory UserModel.fromJson(Map<String, dynamic> json) {
    return UserModel(
      id: json['id'],
      email: json['email'],
      name: json['name'],
      isFirstLogin: json['isFirstLogin'] is int
          ? (json['isFirstLogin'] as int) == 1
          : json['isFirstLogin'] ?? false,
    );
  }

  Map<String, dynamic> toJson() {
    return {
      'id': id,
      'email': email,
      'name': name,
      'isFirstLogin': isFirstLogin ? 1 : 0,
    };
  }
}
